#include <stdio.h>
int main(){
    int i;
    char c ;
    float f ;
    double d ;
    // lu means unassign data types value
    printf("Integer number storage size %lu bytes \n",sizeof(i));
    printf("Character number storage size %lu bytes  \n",sizeof(c));
    printf("Float number storage size %lu bytes  \n",sizeof(f));
    printf("Double number storage size %lu bytes  \n",sizeof(d));

}